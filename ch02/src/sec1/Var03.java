package sec1;

public class Var03 {

	public static void main(String[] args) {
		//primitive type(기본 자료형) : 하나의 데이터만 저장가능한 구조
		boolean a=true;
		//정수
		byte b=20, c=127;	//-128~127 -> 1byte(8bit = 2의8승 = 256가지
		short d=6450;	// 2byte(16) 2의16승 = -2n-1~2n-1-1
		int e=65000;	// 4byte(32)
		long f=650000;	// 8byte(64)
		//실수
		float g=3.14f;	// 4byte(32)
		double h=3.14;	// 8byte(64)
		//문자/문자열
		char i='k';		// 2byte(16) k(공백)
		
		
		String j="kim";	//
		
	}

}
